    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        if (txtAssunto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Inserir um assunto.", "Erro!!!", JOptionPane.WARNING_MESSAGE);
        } else {
            Principal principal = new Principal();
            ModuloEscrita mensagem = new ModuloEscrita();
            mensagem.carregarArq(txtAssunto.getText());
            mensagem.Escrever_Arq(textMensagem.getText());
            JOptionPane.showMessageDialog(null, "Mensagem enviado com sucesso!!!");
            this.dispose();
            principal.setVisible(true);
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

